#include "main.h"
/**
 * swap_int - swaps the values of two integers
 * @a:  parameter
 * @b: parameter
 * Description: swaps the values of two integers
 * Return: values swaped
 */
void swap_int(int *a, int *b)
{
	int n;
	int v;
	int temp;

	a = &n;
	b = &v;
	temp= 0;

	*a = temp;
	*b = *a;
	*a = temp;
}
