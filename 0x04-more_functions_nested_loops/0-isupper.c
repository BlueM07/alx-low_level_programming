/**
 * isupper - checks for uppercase character
 * @c: parameter
(*
* Description: prints a specific string
(* section header)
* Return: 1 if uppser case, 0 if else
*/

int _isupper(int c)
	{
		int c;

		if (c >= 'A' && c <= 'Z')
		{
			return (1);
		}		
		else 
		{
		return (0);
		}		
}	
